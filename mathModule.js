function add(a, b) {
    return a + b;
  }
  
  function subtract(a, b) {
    return a - b;
  }
  
  function multiply(a, b) {
    return a * b;
  }
  
  function divide(a, b) {
    return a / b;
  }
  
  function sqrt(num) {
    return Math.sqrt(num);
  }
  
  function max(a, b) {
    return Math.max(a, b);
  }
  
  module.exports = {
    add,
    subtract,
    multiply,
    divide,
    sqrt,
    max
  };